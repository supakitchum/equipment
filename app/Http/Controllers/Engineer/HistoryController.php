     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $results = TaskCalEquipment::leftjoin('equipments','task_cal_equipments.equipment_id','=','equipments.id')
            ->leftjoin('task_cal_logs','task_cal_equipments.id','=','task_cal_logs.task_id')
            ->select('task_cal_equipments.*','equipments.name as equipment_name','equipments.code','task_cal_logs.assign_date','task_cal_logs.complete_date')
            ->where('user_id',auth()->user()->id)
            ->get();
        return view('engineer.history.index')->with([
            'results' => $results
        ]);
    }

